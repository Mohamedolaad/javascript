var colour = "green";

switch (colour) {
  case "red":
    console.log("The colour is red");
    break;

  case "blue":
    console.log("The colour is blue");
    break;

  case "green":
    console.log("The colour is green");
    break;

  case "yellow":
    console.log("The colour is yellow");
    break;

  default:
    console.log("The colour is neither red, blue, green, nor yellow");
}
